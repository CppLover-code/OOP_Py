# Преобразование списков и кортежей в словарь
"""
Несмотря на то, что словарь и список - непохожие по структуре типы, 
но тем не менее существует возможности для отдельных видов списков 
преобразования их в словарь с помощью встроенной функции dict(). Для 
этого список должен хранить набор вложенных списков. Каждый вложенный 
список должен состоять из двух элементов - при конвертации в словарь 
первый элемент станет ключом, а второй - значением
"""
users_list = [
    ["+111123455", "Tom"],
    ["+384767557", "Bob"],
    ["+958758767", "Alice"]
]
print(users_list)
users_dict = dict(users_list)
print(users_dict)      # {"+111123455": "Tom", "+384767557": "Bob", "+958758767": "Alice"}

users_tuple = (
    ("+111123455", "Tom"),
    ("+384767557", "Bob"),
    ("+958758767", "Alice")
)
users_dict1 = dict(users_tuple)
print(users_dict1)

# Получение и изменение элементов
# dictionary[ключ]

users = {
    "546789": "Tom",
    "123456": "Bob",
    "555878": "Alice"
}
print(users["546789"]) # получаем элемент с ключом
users["999777"] = "Will" # добавляем ключ и значение
print(users)

# поиск ключа в словаре
key = "+4444444"
if key in users:
    user = users[key]
    print(user)
else:
    print("Элемент не найден")

